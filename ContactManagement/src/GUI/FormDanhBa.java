    private void btn_TimKiemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_TimKiemActionPerformed

          // Lấy từ khóa tìm kiếm từ trường văn bản
    String keyword = txt_TimKiem.getText();

    // Lấy chỉ mục của mục được chọn trong combobox
    int selectedIndex = cbb_TimKiem.getSelectedIndex();

        // Tạo một biến để lưu trữ tên cột cần tìm kiếm
        String columnName = "";

        // Xác định tên cột cần tìm kiếm dựa trên chỉ mục của mục được chọn trong combobox
        switch (selectedIndex) {
                     
            case 0:
                columnName = "Ten";
                break;
            case 1:
                columnName = "SoDienThoai";
                break;
            case 2:
                columnName = "Email";
                break;
            case 3:
                columnName = "DiaChi";
                break;
            case 4:
                columnName = "NgaySinh";
                break;       
            default:
                break;
        }

        // Gọi phương thức tìm kiếm từ lớp DAO dựa trên tên cột và từ khóa tìm kiếm
        ArrayList<DanhBa> dsDB = DanhBaDAO.timKiemDanhBa(columnName, keyword, maTaiKhoan);

        // Cập nhật bảng hiển thị danh bạ với kết quả tìm kiếm
        updateTable(dsDB);
    }//GEN-LAST:event_btn_TimKiemActionPerformed

