    private void btn_NhapActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_NhapActionPerformed
        // TODO add your handling code here:
        // Mở hộp thoại chọn file
        JFileChooser fileChooser = new JFileChooser();
        fileChooser.setDialogTitle("Open");
        fileChooser.setFileFilter(new FileNameExtensionFilter("Excel Files", "xlsx"));
        int userSelection = fileChooser.showOpenDialog(null);

        if (userSelection == JFileChooser.APPROVE_OPTION) {
            String filePath = fileChooser.getSelectedFile().getAbsolutePath();

            // Nhập dữ liệu từ file Excel
            List<DanhBa> danhBaList = DanhBaDAO.importFromExcel(filePath);

            // Cập nhật cơ sở dữ liệu hoặc giao diện với dữ liệu đã nhập
            for (DanhBa danhBa : danhBaList) {
                // Gọi phương thức thêmDanhBa từ lớp DAO và kiểm tra kết quả
                if (DanhBaDAO.themDanhBa(danhBa,maTaiKhoan)) {
                    // Cập nhật giao diện hoặc cơ sở dữ liệu
                    System.out.println("Thêm dữ liệu thành công: " + danhBa.getTen());

                } else {
                    System.out.println("Thêm dữ liệu thất bại: " + danhBa.getTen());
                }
            }

            // Load lại bảng dữ liệu trên giao diện
            LoadTable();
            txtMaLienHe.setText(String.valueOf(DanhBaDAO.getMaxMaLienHeFromDatabase() + 1));
            // Hiển thị thông báo kết quả
            JOptionPane.showMessageDialog(null, "Nhập dữ liệu thành công!", "Thông báo", JOptionPane.INFORMATION_MESSAGE);
        } else {
            JOptionPane.showMessageDialog(null, "Hủy chọn file.", "Thông báo", JOptionPane.INFORMATION_MESSAGE);
        }
    }//GEN-LAST:event_btn_NhapActionPerformed

