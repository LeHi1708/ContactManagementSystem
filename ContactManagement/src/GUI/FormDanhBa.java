    private void LoadTable() {
        this.setLocationRelativeTo(null);
        String[] tieuDe = {"Mã Liên Hệ", "Họ Tên", "Số Điện Thoại", "Email", "Địa chỉ", "Ngày Sinh"};
        dtm.setColumnIdentifiers(tieuDe);
        ArrayList<DanhBa> dsDB = DanhBaDAO.showdsDB(maTaiKhoan); // Sử dụng mã tài khoản
        dtm.setRowCount(0);
        for (DanhBa db : dsDB) {
            Vector<Object> vec = new Vector<Object>();
            vec.add(db.getMaLienHe());
            vec.add(db.getTen());
            vec.add(db.getSoDienThoai());
            vec.add(db.getEmail());
            vec.add(db.getDiaChi());
            vec.add(db.getNgaySinh());
            dtm.addRow(vec);
        }
        dsDanhBa.setModel(dtm);
        
        dsDanhBa.getColumnModel().getColumn(0).setMinWidth(0);
        dsDanhBa.getColumnModel().getColumn(0).setMaxWidth(0);
        dsDanhBa.getColumnModel().getColumn(0).setWidth(0);
    }
     
    
