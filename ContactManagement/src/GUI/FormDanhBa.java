    private void btn_AddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_AddActionPerformed
          // TODO add your handling code here:
        int malienhe = Integer.parseInt(txtMaLienHe.getText());
        String ten = txtHoTen.getText(); // Lấy dữ liệu từ trường tên
        int soDienThoai = 0;
        String email = txtEmail.getText(); // Lấy dữ liệu từ trường email
        String diaChi = txtDiaChi.getText(); // Lấy dữ liệu từ trường địa chỉ
        String ngaySinhStr = txtNgaySinh.getText();

        // Kiểm tra xem trường Tên có được nhập hay không
        if (ten.isEmpty()) {
            JOptionPane.showMessageDialog(null, "Vui lòng nhập Tên!");
            return; // Dừng xử lý tiếp theo nếu không có Tên
        }
         if (diaChi.isEmpty()) {
            JOptionPane.showMessageDialog(null, "Vui lòng nhập Địa chỉ!");
            return; // Dừng xử lý tiếp theo nếu không có Tên
        }

        // Kiểm tra xem trường số điện thoại có chứa dữ liệu hợp lệ hay không
        if (!txtSoDienThoai.getText().isEmpty()) {
            try {
                soDienThoai = Integer.parseInt(txtSoDienThoai.getText());
            } catch (NumberFormatException e) {
                JOptionPane.showMessageDialog(null, "Số điện thoại không hợp lệ!");
                return; // Dừng xử lý tiếp theo nếu số điện thoại không hợp lệ
            }
        } else {
            JOptionPane.showMessageDialog(null, "Vui lòng nhập số điện thoại!");
            return; // Dừng xử lý tiếp theo nếu không có số điện thoại
        }

        // Kiểm tra xem email có chứa ký tự "@" hay không
        if (!email.contains("@")) {
            JOptionPane.showMessageDialog(null, "Email không hợp lệ!");
            return; // Dừng xử lý tiếp theo nếu email không hợp lệ
        }

        // Kiểm tra xem ngày sinh có được nhập đúng định dạng hay không
        Date ngaySinh = null;
        try {
            SimpleDateFormat sdf = new SimpleDateFormat("dd-MM-yyyy"); // Định dạng ngày
            ngaySinh = sdf.parse(ngaySinhStr); // Chuyển chuỗi thành đối tượng Date
        } catch (ParseException ex) {
            // Xử lý nếu không thể chuyển đổi chuỗi thành ngày
            JOptionPane.showMessageDialog(null, "Ngày sinh không hợp lệ!");
            return; // Dừng xử lý tiếp theo nếu ngày sinh không hợp lệ
        }

        // Kiểm tra ngày sinh đã được chuyển đổi thành công hay không
        if (ngaySinh != null) {
            // Tạo đối tượng DanhBa từ dữ liệu lấy được
            DanhBa newContact = new DanhBa(malienhe, ten, soDienThoai, email, diaChi, ngaySinh);
            // Gọi phương thức thêmDanhBa từ lớp DAO và kiểm tra kết quả
            if (DanhBaDAO.themDanhBa(newContact, maTaiKhoan)) {
                // Thông báo thành công nếu thêm thành công
                JOptionPane.showMessageDialog(null, "Thêm dữ liệu thành công!");
                // Xóa nội dung của các trường văn bản
                txtMaLienHe.setText(String.valueOf(DanhBaDAO.getMaxMaLienHeFromDatabase() + 1));
                txtHoTen.setText("");
                txtSoDienThoai.setText("");
                txtEmail.setText("");
                txtDiaChi.setText("");
                txtNgaySinh.setText("ex: 03-03-2003");
                txtNgaySinh.setForeground(new Color(153, 153, 153));
            } else {
                // Thông báo lỗi nếu thêm không thành công
                JOptionPane.showMessageDialog(null, "Thêm dữ liệu thất bại!");
            }
        } else {
            JOptionPane.showMessageDialog(null, "Lỗi ngày sinh không hợp lệ");
        }

        LoadTable();
    }//GEN-LAST:event_btn_AddActionPerformed

