    private void updateTable(ArrayList<DanhBa> dsDB) {
        // Lấy mô hình bảng hiện tại
        DefaultTableModel model = (DefaultTableModel) dsDanhBa.getModel();

        // Xóa tất cả các hàng hiện tại trong bảng
        model.setRowCount(0);

        // Thêm các hàng mới vào bảng
        for (DanhBa db : dsDB) {
            model.addRow(new Object[]{db.getMaLienHe(), db.getTen(), db.getSoDienThoai(), db.getEmail(), db.getDiaChi(), db.getNgaySinh()});
        }
    }
    private void selectionModel()
    {
            //Thêm sự kiện lắng nghe cho bảng
        dsDanhBa.getSelectionModel().addListSelectionListener(new ListSelectionListener() {
        @Override
        public void valueChanged(ListSelectionEvent event) {
            if (!event.getValueIsAdjusting()) {
                //Lấy hàng được chọn
                int selectedRow = dsDanhBa.getSelectedRow();
                //Kiểm tra nếu có hàng được chọn
                if (selectedRow != -1) {
                    //Lấy dữ liệu từ hàng được chọn và hiển thị lên các trường văn bản tương ứng
                    txt_mlh.setText(dsDanhBa.getValueAt(selectedRow, 0).toString()); 
                    txtht.setText(dsDanhBa.getValueAt(selectedRow, 1).toString()); 
                    txtsdt.setText(dsDanhBa.getValueAt(selectedRow, 2).toString()); 
                    txte.setText(dsDanhBa.getValueAt(selectedRow, 3).toString()); 
                    txtdc.setText(dsDanhBa.getValueAt(selectedRow, 4).toString()); 
                    txtns.setText(dsDanhBa.getValueAt(selectedRow, 5).toString()); 
                    //Có thể tiếp tục với các trường khác tương tự
                }
            }
        }
    });
    }
