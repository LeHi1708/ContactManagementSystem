    private void btn_CapNhatActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_CapNhatActionPerformed
        // TODO add your handling code here:
        // Lấy thông tin mới từ các trường văn bản
        int maLienHe = Integer.parseInt(txt_mlh.getText());
        String ten = txtht.getText();
        int soDienThoai = Integer.parseInt(txtsdt.getText());
        String email = txte.getText();
        String diaChi = txtdc.getText();
        String ngaySinhStr = txtns.getText();

        // Chuyển đổi ngày sinh từ chuỗi sang đối tượng Date
        Date ngaySinh = null;
        try {
            ngaySinh = new SimpleDateFormat("dd-MM-yyyy").parse(ngaySinhStr);
        } catch (ParseException ex) {
            ex.printStackTrace();
            // Hiển thị thông báo lỗi nếu ngày sinh không hợp lệ
            JOptionPane.showMessageDialog(null, "Lỗi! Ngày sinh không hợp lệ.");
            return; // Dừng việc cập nhật nếu có lỗi xảy ra
        }

        // Tạo đối tượng DanhBa mới với thông tin đã cập nhật
        DanhBa updatedContact = new DanhBa(maLienHe, ten, soDienThoai, email, diaChi, ngaySinh);

        // Gọi phương thức cập nhật từ lớp DAO và kiểm tra kết quả
        if (DanhBaDAO.capNhatDanhBa(updatedContact)) {
            // Hiển thị thông báo cập nhật thành công
            JOptionPane.showMessageDialog(null, "Cập nhật thông tin thành công!");
        } else {
            // Hiển thị thông báo lỗi nếu cập nhật không thành công
            JOptionPane.showMessageDialog(null, "Lỗi! Không thể cập nhật thông tin.");
        }

        // Cập nhật lại bảng
        LoadTable();
    }//GEN-LAST:event_btn_CapNhatActionPerformed

